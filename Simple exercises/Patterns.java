public class Patterns
{
	public static void main(String[] args) 
	{   //Exercises using System.out.println()
		System.out.println("Print Checker Pattern:");
		System.out.println("\n***********");
		System.out.println(" ***********");
		System.out.println("***********");
		System.out.println(" ***********");
		System.out.println("***********");
		
		System.out.println("Print Square Pattern:");
		System.out.println("*****");
		System.out.println("*   *");
		System.out.println("*   *");
		System.out.println("*****");
		System.out.println();
		
		System.out.println("Print Triangular Pattern:");
		System.out.println();
		System.out.println("* * * * *");
		System.out.println(" *     *");
		System.out.println("  *   *");
		System.out.println("   * *");
		System.out.println("    *");
		System.out.println();
		
		System.out.println("Print Star Pattern:");
		System.out.println();
		System.out.println("  *  ");
		System.out.println("** **");
		System.out.println(" * *");
		System.out.println("*   *");
		//System.out.println("*    *");
}
}
